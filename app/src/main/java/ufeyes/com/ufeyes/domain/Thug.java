public class Thug {



    private String id;
    private String idOccorrence;
    private int skinColor;
    private int weaponType;
    private int clothingColor;
    private int stature;
    private int vehicle;


    public String getIdOccorrence() {
        return idOccorrence;
    }

    public void setIdOccorrence(String idOccorrence) {
        this.idOccorrence = idOccorrence;
    }

    public int getSkinColor() {
        return skinColor;
    }

    public void setSkinColor(int skinColor) {
        this.skinColor = skinColor;
    }

    public int getWeaponType() {
        return weaponType;
    }

    public void setWeaponType(int weaponType) {
        this.weaponType = weaponType;
    }

    public int getClothingColor() {
        return clothingColor;
    }

    public void setClothingColor(int clothingColor) {
        this.clothingColor = clothingColor;
    }

    public int getStature() {
        return stature;
    }

    public void setStature(int stature) {
        this.stature = stature;
    }

    public int getVehicle() {
        return vehicle;
    }

    public void setVehicle(int vehicle) {
        this.vehicle = vehicle;
    }
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


}
